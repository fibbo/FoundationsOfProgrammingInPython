def add_two(value):
    result = value + 2
    return result

def multiply_by_two(value):
    result = value * 3
    return result

def divide_by_two(value):
    result = value / 2
    return result


number = 16

number = add_two(number)
number = multiply_by_two(number)
number = divide_by_two(number)

print(number)
