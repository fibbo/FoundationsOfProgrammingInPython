text_2 = "Python is great programming language"

text_length = len(text)

sum_of_characters = len(text) + len(text_2)

print(f"The text variable contains {text_length} characters including whitespace")
print(f"The text variable contains {len(text)} characters including whitespace")

print(
    f"The text and text_2 variables contain {sum_of_characters} characters including whitespace"
)
